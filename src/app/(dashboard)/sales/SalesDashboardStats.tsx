function StatsSkeleton() {
  return (
    <>
      {[1, 2, 3, 4].map((i) => (
        <div key={i} className={styles.statsSkeleton} />
      ))}
    </>
  );
}

export function SalesDashboardStats() {
  const { data, isLoading } = useSalesDashboard();

  if (isLoading) return <StatsSkeleton />;
  if (!data) return null;

  const rate = data.conversion_rate;
  const convDisplay = rate === 0 ? '0%' : `${(rate <= 1 ? rate * 100 : rate).toFixed(1)}%`;

  return (
    <>
      <StatsCard label="Total Leads" value={data.total_leads} icon={<TotalLeadsIcon />} />
      <StatsCard label="New Leads" value={data.new_leads} icon={<NewLeadsIcon />} />
      <StatsCard label="Calls Done" value={data.calls_done} icon={<CallsIcon />} />
      <StatsCard label="Conv. Rate" value={convDisplay} icon={<ConversionIcon />} />
    </>
  );
}
